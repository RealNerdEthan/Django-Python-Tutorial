# Practicing more if else statments
volume = 57
if volume < 20:
    print("It's kind of quiet.")
elif 20 <= volume < 40:
    print("It's nice for background music.")
elif 40 <= volume < 60:
    print("Perfect, I can hear the details!")
elif 60 <= volume < 80:
    print("Nice for parties :)")
elif 80 <= volume < 100:
    print("A bit loud!")
else:
    print("I can't hear anything?")
